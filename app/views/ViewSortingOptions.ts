/**
 * Enum representing the available sorting options for a view.
 */
export enum ViewSortingOptions {

    /**
     * Sort by the order of occurrence of an item in the view.
     */
    Oldest = "Oldest",
    /**
     * Show newest items first. 
     */
    Newest = "Newest",
    /**
     * Sort by the date of the item in the view.
     */
    Date = "Date"
}